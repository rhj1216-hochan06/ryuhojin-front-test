export const getTourismContentTypeCode = (
  contentTypeId: string | undefined,
): TourismContentTypeCode => {
  if (!contentTypeId) {
    return {
      contentTypeId: '-',
      multilingualCode: '-',
      name: '콘텐츠 타입 미분류',
    };
  }

  return (
    tourismContentTypeCodes[contentTypeId] ?? {
      contentTypeId,
      multilingualCode: '-',
      name: `기타 (${contentTypeId})`,
    }
  );
};

export const getTourismClassificationSystem = ({
  lclsSystm1,
  lclsSystm2,
  lclsSystm3,
}: TourismClassificationSearchParams): TourismClassificationSystem | null => {
  if (!lclsSystm1 && !lclsSystm2 && !lclsSystm3) {
    return null;
  }

  const exactMatch = tourismClassificationSystems.find((item) =>
    lclsSystm3 ? item.lclsSystm3 === lclsSystm3 : false,
  );

  if (exactMatch) {
    return exactMatch;
  }

  const middleMatch = tourismClassificationSystems.find((item) =>
    lclsSystm2 ? item.lclsSystm2 === lclsSystm2 : false,
  );

  if (middleMatch) {
    return middleMatch;
  }

  return (
    tourismClassificationSystems.find((item) =>
      lclsSystm1 ? item.lclsSystm1 === lclsSystm1 : false,
    ) ?? null
  );
};

export const getTourismClassificationPath = (
  classification: TourismClassificationSystem | null,
): string => {
  if (!classification) {
    return '분류체계 정보 없음';
  }

  return `${classification.lclsSystm1Name} > ${classification.lclsSystm2Name} > ${classification.lclsSystm3Name}`;
};
